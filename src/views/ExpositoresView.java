    private void obtenerDatos(String obD){
        ConnectSQLite sql = new ConnectSQLite();
        String[] expDat = new String[4];
        String[] expDatT = new String[4];
        expDatT = sql.getDatosExpositor(obD);
        if(obD != null){    
            for (int i = 0; i <= 3 ; i++){
                if(i < 3){
                expDat[i]=expDatT[i].toUpperCase();
                } else{
                    expDat[i]=expDatT[i];
                }
            }
        }
            
        if(expDat != null){ 
            giroLabel.setText(expDat[0]); 
            respLabel.setText(expDat[1]); 
            telLabel.setText(expDat[2]); 
            mailLabel.setText(expDat[3]); 
        } else{
            System.out.println("no hay datos");
        }
        
        
    }
